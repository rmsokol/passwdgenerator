const pwbtn = document.getElementById("pass-btn")
let passwordLength = 15
// function to get random character
function getRandomchar() {
    let randomIndex = Math.floor(Math.random() * characters.length)
    return characters[randomIndex]
}
// function to generate random character for range of desired length (passwordLength)
function generatePassword() {
    let randomPassword = ""
    for (i = 0; i < passwordLength; i++){
        randomPassword += getRandomchar()   
    } 
    return randomPassword
}
//display generated password w/ 2 new variables generated by generatePassword() function passed to document element
function render() {
    let randomPasswordOne = generatePassword()
    let randomPasswordTwo = generatePassword()
    document.getElementById("pass-1").innerHTML = randomPasswordOne
    document.getElementById("pass-2").innerHTML = randomPasswordTwo
}




